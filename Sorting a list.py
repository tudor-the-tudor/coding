#Sorting a list of strings in alphabetical order:
programming_languages = ["Python", "C++", "C", "Java", "Javascript"]
programming_languages.sort()
print(programming_languages)

#Sorting a list of integers:
my_numbers = [100, 3, 16, 708, 19736, 16533, 1798363696, 179836369836]
my_numbers.sort()
print(my_numbers)

#Sorting a list of strings in reverse alphabetical order:
programming_languages = ["Python", "C++", "C", "Java", "Javascript"]
programming_languages.sort(reverse=True)
print(programming_languages)

#Sorting a list of integers in reverse order:
my_numbers = [100, 3, 16, 708, 19736, 16533, 1798363696, 179836369836]
my_numbers.sort(reverse=True)
print(my_numbers)

